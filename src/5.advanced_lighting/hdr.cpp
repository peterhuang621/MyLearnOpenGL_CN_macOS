        shader.use();
        shader.setMat4("projection", projection);
        shader.setMat4("view", view);

        model = glm::mat4(1.0f);
        model = glm::rotate(model, glm::radians((float)glfwGetTime() * (-10.0f)),
                            glm::normalize(glm::vec3(1.0f, 0.0, 1.0f)));
